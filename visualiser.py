#        VISUALIZATION FUNCTIONS
# ==========================================

def show_structure(file_path_or_string, width=800, height=400, show_box="auto"):
    """
    Visualize a static MOL, PDB, or GRO structure with one consistent style.

    Examples
    --------
    view = show_structure("monomer.mol")
    view = show_structure("polymer.pdb")
    view = show_structure("bulk.gro", show_box=True)
    view.show()
    """
    view = py3Dmol.view(width=width, height=height)

    ext = "pdb"
    box_nm = None

    if os.path.exists(file_path_or_string):
        ext = file_path_or_string.split(".")[-1].lower()

        if ext == "gro":
            data, box_nm = _gro_to_pdb_block(file_path_or_string)
            fmt = "pdb"

        else:
            with open(file_path_or_string, "r") as handle:
                data = handle.read()

            if ext == "mol":
                fmt = "mol"
            else:
                data = _fix_pdb_elements(data)
                fmt = "pdb"

    else:
        # Assume raw molecular text.
        data = file_path_or_string
        fmt = "mol" if data.lstrip().startswith("@<TRIPOS>") else "pdb"
        if fmt == "pdb":
            data = _fix_pdb_elements(data)

    view.addModel(data, fmt)
    apply_custom_style(view)

    if show_box is True or (show_box == "auto" and ext == "gro"):
        _add_periodic_box_edges(view, box_nm)

    view.zoomTo()
    return view


def show_trajectory(pdb_content, width=800, height=400):
    """
    Visualize an animated PDB trajectory.
    This remains separate because trajectories use addModelsAsFrames().
    """
    view = py3Dmol.view(width=width, height=height)

    view.addModelsAsFrames(_fix_pdb_elements(pdb_content), 'pdb')
    apply_custom_style(view)

    view.animate({'loop': 'forward', 'reps': 50, 'step': 1, 'interval': 60})
    view.zoomTo()

    return view
